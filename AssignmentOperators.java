public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;
        a = a + 10;
        System.out.println(a);
        a += 10;
        System.out.println(a);
        int b = 78;
        b -= 8;
        System.out.println(b);
        int k = 6;
        k *= k;
        System.out.println(k);
        k /= 6;
        System.out.println(k);
        int x = 78;
        x %= 7;
        System.out.println(x);
    }
}
