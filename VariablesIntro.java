public class VariablesIntro {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println('a');
        System.out.println(a);
        System.out.println(b);
        a = 50;
        b = 112;
        System.out.println(a);
        a = b;
        System.out.println(a);
        String name = "Eklas Mohin";
        System.out.println(name);
    }
}