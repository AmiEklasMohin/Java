public class IntroductionToVariables {
    public static void main(String[] args) {
        int x = 10;
        int y = 55;
        System.out.println(x);
        System.out.println(y);
        System.out.println('x');
        x = y;
        System.out.println(x);
        String myName = "Eklas Mohin";
        System.out.println(myName);
    }
}